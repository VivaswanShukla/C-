#include<iostream>
using namespace std;

int main(int argc, char const *argv[])
{
    int a = 5;
    int b = 6;

    // Comparison Operators
    cout<<"Is a > b: "<< (a > b) << endl;
    cout<<"Is a >= b: "<< (a >= b) << endl;
    cout<<"Is a < b: "<< (a < b) << endl;
    cout<<"Is a <= b: "<< (a <= b) << endl;
    cout<<"Is a == b: "<< (a == b) << endl;
    cout<<"Is a != b: "<< (a != b) << endl;
    return 0;
}
